package GeneticAlgorithm;

public class Couple {

	private Integer A;
	private Integer B;
	
	public Couple(Integer a, Integer b) {
		super();
		A = a;
		B = b;
	}

	public Integer getA() {
		return A;
	}

	public void setA(Integer a) {
		A = a;
	}

	public Integer getB() {
		return B;
	}

	public void setB(Integer b) {
		B = b;
	}
	
	
}
